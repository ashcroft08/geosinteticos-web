// ===== RENDERIZADORES =====
const Components = {
    // Renderizar card de producto
    productCard(product) {
        const imagenPrincipal = product.imagenes.find(img => img.esPrincipal) || product.imagenes[0];
        return `
      <article class="product-card" data-id="${product.id}">
        <div class="product-card__image">
          <img src="${imagenPrincipal?.url || 'https://via.placeholder.com/400x300'}" alt="${imagenPrincipal?.alt || product.nombre}" loading="lazy">
          ${product.disponible ? '<span class="product-card__badge badge badge--success">Disponible</span>' : ''}
        </div>
        <div class="product-card__body">
          <span class="product-card__category">${product.subcategoria}</span>
          <h3 class="product-card__title">${product.nombre}</h3>
          <p class="product-card__description">${product.descripcionCorta}</p>
          <div class="product-card__footer">
            <a href="#/producto/${product.id}" class="btn btn--primary btn--sm">Ver Detalles</a>
            <button class="btn btn--outline btn--sm" onclick="App.openQuoteModal('${product.nombre}')">Cotizar</button>
          </div>
        </div>
      </article>
    `;
    },

    // Renderizar card de proyecto
    projectCard(project) {
        const imagenPrincipal = project.galeria[0];
        return `
      <article class="project-card" onclick="App.navigate('#/proyecto/${project.id}')">
        <img src="${imagenPrincipal?.url || 'https://via.placeholder.com/400x300'}" alt="${project.titulo}" class="project-card__image" loading="lazy">
        <div class="project-card__overlay">
          <span class="project-card__category">${project.tipoDeObra}</span>
          <h3 class="project-card__title">${project.titulo}</h3>
          <p class="project-card__location">${ICONS.mapPin} ${project.ubicacion.ciudad}, ${project.ubicacion.region}</p>
        </div>
      </article>
    `;
    },

    // Renderizar feature card
    featureCard(feature) {
        return `
      <div class="feature-card">
        <div class="feature-card__icon">${feature.icon}</div>
        <h4 class="feature-card__title">${feature.title}</h4>
        <p class="feature-card__description">${feature.description}</p>
      </div>
    `;
    },

    // Renderizar estadísticas
    statItem(stat) {
        return `
      <div class="stat">
        <div class="stat__number">${stat.numero}</div>
        <div class="stat__label">${stat.label}</div>
      </div>
    `;
    },

    // Renderizar service card
    serviceCard(service) {
        const items = service.items.map(item => `<li>${ICONS.check} ${item}</li>`).join('');
        return `
      <div class="service-card">
        <div class="service-card__number">${service.numero}</div>
        <h3 class="service-card__title">${service.titulo}</h3>
        <p class="service-card__description">${service.descripcion}</p>
        <ul class="service-card__list">${items}</ul>
        <button class="btn btn--primary" onclick="App.openQuoteModal('${service.titulo}')">Consultar</button>
      </div>
    `;
    },

    // Renderizar certification card
    certificationCard(cert) {
        return `
      <div class="certification-card">
        <div class="certification-card__logo">
          <div style="width: 60px; height: 60px; background: var(--color-accent); border-radius: 50%; display: flex; align-items: center; justify-content: center;">
            ${ICONS.shield}
          </div>
        </div>
        <h4 class="certification-card__name">${cert.nombre}</h4>
        <p class="certification-card__validity">Vigencia: ${cert.vigencia}</p>
      </div>
    `;
    },

    // Value Card
    valueCard(value) {
        return `
      <div class="value-card">
        <div class="value-card__icon">${value.icon}</div>
        <h4 class="value-card__title">${value.title}</h4>
      </div>
    `;
    },

    // Tabla de especificaciones
    specsTable(specs) {
        const rows = specs.map(spec => `
      <tr>
        <th>${spec.propiedad}</th>
        <td>${spec.valor} ${spec.unidad}</td>
      </tr>
    `).join('');
        return `<table class="specs-table"><tbody>${rows}</tbody></table>`;
    },

    // Applications list
    applicationsList(apps) {
        return `
      <div class="applications-list">
        ${apps.map(app => `<span class="application-tag">${ICONS.check} ${app}</span>`).join('')}
      </div>
    `;
    },

    // Toast notification
    toast(message, type = 'success') {
        const toast = document.createElement('div');
        toast.className = `toast toast--${type}`;
        toast.innerHTML = `
      ${type === 'success' ? ICONS.check : ICONS.close}
      <span>${message}</span>
    `;

        let container = document.querySelector('.toast-container');
        if (!container) {
            container = document.createElement('div');
            container.className = 'toast-container';
            document.body.appendChild(container);
        }

        container.appendChild(toast);
        setTimeout(() => toast.remove(), 4000);
    }
};

// Exponer globalmente
window.Components = Components;
